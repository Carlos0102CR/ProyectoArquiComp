ORG 1000H
ROJ DB 0
AMA DB 1
VER DB 1

MSJFN DB "El funcionamiento es normal"
FINMSJFN DB ?

MSJFA DB "El funcionamiento es anormal"
FINMSJFA DB ?

ORG 2000H
MOV AH,ROJ
MOV AL,AMA
CMP AH,AL
JC AMAENCENDIDO
JZ IGUALES
JNZ RJENCENDIDO

AMAENCENDIDO: MOV AH,1
MOV AL,VER
AND AH,AL
CMP AH,0
JZ NORMAL
CMP AH,1
JZ ANORMAL

IGUALES: AND AH,AL
MOV AL,VER
SUB AH,AL
CMP AH,0
JZ ANORMAL
CMP AH,1
JZ NORMAL
CMP AH,AL
JS NORMAL

RJENCENDIDO: MOV AH,1
MOV AL,VER
AND AH,AL
CMP AH,0
JZ NORMAL
CMP AH,1
JZ ANORMAL


NORMAL: MOV BX, OFFSET MSJFN
MOV AL, OFFSET FINMSJFN-OFFSET MSJFN
INT 7
INT 0

ANORMAL: MOV BX, OFFSET MSJFA
MOV AL, OFFSET FINMSJFA-OFFSET MSJFA
INT 7
INT 0

END



